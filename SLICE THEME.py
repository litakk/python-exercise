# В Python оператор среза (slice) позволяет вам извлекать подстроки,
# подсписки или подпоследовательности из последовательности 
# (например, строки, списки, кортежи и т. д.). Он имеет следующий синтаксис:

# ```python
# sequence[начало:конец:шаг]
# ```

# - `начало` - начальный индекс среза (включительно)
# - `конец` - конечный индекс среза (не включительно)
# - `шаг` - шаг, с которым вы хотите извлекать элементы из последовательности (по умолчанию 1)

# Примеры:
# ```python
my_list = [1, 2, 3, 4, 5]
print(my_list[1:4])  # Выводит [2, 3, 4]
print(my_list[::2])  # Выводит [1, 3, 5]
# ```

# Индексы могут быть отрицательными, начиная с -1 (последний элемент), -2 (предпоследний элемент) и так далее.

# Если вам нужен только один элемент из последовательности по индексу, просто используйте индексирование:

# ```python
my_list = [1, 2, 3, 4, 5]
print(my_list[2])  # Выводит 3
# ```

# Если вы хотите выполнить срез с определенным индексом и получить один элемент, вы можете использовать следующий синтаксис:

# ```python
my_list = [1, 2, 3, 4, 5]
print(my_list[2:3][0])  # Выводит 3
# ``` 

